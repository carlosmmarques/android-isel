package pt.isel.pdm.li51n.g4.tmdbisel.data.provider.TMDb;

public class TMDbAPIRateLimitException extends Exception{
    /**
     * Constructs a new {@code Exception} with the current stack trace and the
     * specified detail message.
     *
     * @param detailMessage the detail message for this exception.
     */
    public TMDbAPIRateLimitException(String detailMessage) {
        super(detailMessage);
    }

    /**
     * Constructs a new {@code Exception} with the current stack trace, the
     * specified detail message and the specified cause.
     *
     * @param detailMessage the detail message for this exception.
     * @param throwable
     */
    public TMDbAPIRateLimitException(String detailMessage, Throwable throwable) {
        super(detailMessage, throwable);
    }

    /**
     * Constructs a new {@code Exception} with the current stack trace and the
     * specified cause.
     *
     * @param throwable the cause of this exception.
     */
    public TMDbAPIRateLimitException(Throwable throwable) {
        super(throwable);
    }
}
